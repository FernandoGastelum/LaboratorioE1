    private void metodosIniciales() throws Exception {
        this.cargarConfiguracionInicialTabla();
        this. buscarClienteParaTabla();
    }

    private void cargarConfiguracionInicialTabla() {
        ActionListener onEditarClickListener = (ActionEvent e) -> {
            //Metodo para editar
            editar();
        };
        int indiceColumnaEditar = 4;
        TableColumnModel modeloColumnas = this.tblClientes.getColumnModel();
        modeloColumnas.getColumn(indiceColumnaEditar)
                .setCellRenderer(new JButtonRenderer("Editar"));
        modeloColumnas.getColumn(indiceColumnaEditar)
                .setCellEditor(new JButtonCellEditor("Editar", onEditarClickListener));

        ActionListener onEliminarClickListener = (ActionEvent e) -> {
            //Metodo para eliminar
            eliminar();
        };
        int indiceColumnaEliminar = 5;
        modeloColumnas = this.tblClientes.getColumnModel();
        modeloColumnas.getColumn(indiceColumnaEliminar)
                .setCellRenderer(new JButtonRenderer("Eliminar"));
        modeloColumnas.getColumn(indiceColumnaEliminar)
                .setCellEditor(new JButtonCellEditor("Eliminar", onEliminarClickListener));
    }

    private int getIdSeleccionadoTabla() {
        int indiceFilaSeleccionada = this.tblClientes.getSelectedRow();
        if (indiceFilaSeleccionada != -1) {
            DefaultTableModel modelo = (DefaultTableModel) this.tblClientes.getModel();
            int indiceColumnaId = 0;
            int idClienteSeleccionado = (int) modelo.getValueAt(indiceFilaSeleccionada,
                    indiceColumnaId);
            return idClienteSeleccionado;
        } else {
            return 0;
        }
    }

    private void editar() {
        int id = this.getIdSeleccionadoTabla();
        System.out.println("El id que se va a editar es " + id);
    }

    private void eliminar() {
        int id = this.getIdSeleccionadoTabla();
        System.out.println("El id que se va a eliminar es " + id);
    }
    
    private void buscarClienteParaTabla() throws Exception {
        System.out.println("Depuraci√≥n: clienteNegocio = " + clienteNegocio);
        
        try {
            List<ClientesTablaDTO> ClientesTablaLista = this.clienteNegocio.buscarClientes();
            this.agregarRegistrosTabla(ClientesTablaLista);
        
        } catch (NegocioException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
     private void agregarRegistrosTabla(List<ClientesTablaDTO> clientesLista) {
        if (clientesLista == null) {
            return;
        }

        DefaultTableModel modeloTabla = (DefaultTableModel) this.tblClientes.getModel();
        clientesLista.forEach(row -> {
            Object[] fila = new Object[5];
            fila[0] = row.getNombre();
            fila[1] = row.getApellidoPaterno();
            fila[2] = row.getApellidoMaterno();
            fila[3] = row.getFechaNacimiento();
            modeloTabla.addRow(fila);
        });
    }

